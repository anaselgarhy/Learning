package loops;

public class ForLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello");
        }
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy New Year!");
    }
}
